/**
 * @file algorithms/geometry/algorithms/simplex-noise/types/points
 * @module algorithms/geometry/algorithms/simplex-noise/types/points
 * @description Point type definitions for Simplex Noise.
 */

/**
 * Represents a 2D point with x and y coordinates.
 */
export interface Point2D {
  x: number;
  y: number;
}

/**
 * Represents a 3D point with x, y, and z coordinates.
 */
export interface Point3D {
  x: number;
  y: number;
  z: number;
}

/**
 * Represents a 4D point with x, y, z, and w coordinates.
 */
export interface Point4D {
  x: number;
  y: number;
  z: number;
  w: number;
}

